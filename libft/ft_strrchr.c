char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*r;

	r = 0;
	i = -1;
	while (s[++i])
	{
		if (s[i] == c)
			r = (char *)s + i;
	}
	if (s[i] == c)
		r = (char *)s + i;
	return (r);
}
